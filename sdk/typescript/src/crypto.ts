// Helpers
// ---------------------------------------------------------------------------

/**
 * Compute the `ath` claim: base64url(SHA-256(leaseJwt)).
 *
 * Binds the DPoP proof to a specific lease token so that a proof intercepted
 * from one request cannot be replayed with a different token.
 */
export async function computeAth(leaseJwt: string): Promise<string> {
  const digest = await crypto.subtle.digest(
    "SHA-256",
    new TextEncoder().encode(leaseJwt),
  );
  return b64url(new Uint8Array(digest));
}

/**
 * Normalize a URI for DPoP `htu` comparison (RFC 9449 §4.2.2).
 *
 * Must match the server's `normalize_htu` in
 * `crates/latchgate-auth/src/dpop/mod.rs` exactly:
 *
 * 1. Strip fragment and query string.
 * 2. Lowercase scheme and authority (path case is preserved).
 * 3. Strip default ports (`:80` for HTTP, `:443` for HTTPS).
 * 4. Bare authority without a path gets `/`.
 */
export function normalizeHtu(uri: string): string {
  // Remove fragment
  uri = uri.split("#")[0] ?? uri;
  // Remove query string
  uri = uri.split("?")[0] ?? uri;

  // Split scheme from the rest
  const sepIdx = uri.indexOf("://");
  if (sepIdx === -1) return uri; // no scheme — return as-is

  const scheme = uri.slice(0, sepIdx).toLowerCase();
  const afterScheme = uri.slice(sepIdx + 3);

  // Split authority from path on the first slash
  const slashIdx = afterScheme.indexOf("/");
  let authority: string;
  let path: string;
  if (slashIdx === -1) {
    authority = afterScheme.toLowerCase();
    path = "/";
  } else {
    authority = afterScheme.slice(0, slashIdx).toLowerCase();
    path = afterScheme.slice(slashIdx); // includes leading '/'
  }

  // Strip default ports — lastIndexOf handles IPv6 bracket-addresses correctly
  authority = stripDefaultPort(authority, scheme);

  return `${scheme}://${authority}${path}`;
}

/**
 * Strip the default port from an authority string (HTTP → 80, HTTPS → 443).
 *
 * Uses `lastIndexOf(":")` so IPv6 bracket-addresses (`[::1]:8080`) are
 * handled correctly — the port separator is always the last colon.
 */
function stripDefaultPort(authority: string, scheme: string): string {
  const idx = authority.lastIndexOf(":");
  if (idx === -1) return authority;
  const host = authority.slice(0, idx);
  const port = authority.slice(idx + 1);
  if (
    (scheme === "http" && port === "80") ||
    (scheme === "https" && port === "443")
  ) {
    return host;
  }
  return authority;
}
